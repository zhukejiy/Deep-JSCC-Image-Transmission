import os
import time


class Visualizer:
    """This class includes several functions that can display/save images and print/save logging information.

    It uses a Python library 'visdom' for display, and a Python library 'dominate' (wrapped in 'HTML') for creating HTML files with images.
    """

    def __init__(self, opt):
        self.opt = opt  # cache the option
        self.name = opt.name
        
        if not os.path.isdir(os.path.join(opt.checkpoints_dir, opt.name)):
            os.makedirs(os.path.join(opt.checkpoints_dir, opt.name))
        # create a logging file to store training losses
        self.log_name = os.path.join(opt.checkpoints_dir, opt.name, 'loss_metric_log.txt')
        with open(self.log_name, "w") as log_file:
            now = time.strftime("%c")
            log_file.write('================ Training Loss and Metric(%s) ================\n' % now)

    # losses: same format as |losses| of plot_current_losses
    def print_current_training(self, epoch, iters, losses, metrics, t_comp, t_data):
        """print current training conditions on console; also save the losses and metrics to the disk

        Parameters:
            epoch (int) -- current epoch
            iters (int) -- current training iteration during this epoch (reset to 0 at the end of every epoch)
            losses (OrderedDict) -- training losses stored in the format of (name, float) pairs
            metrics (OrderedDict) -- training metrics stored in the format of (name, float) pairs
            t_comp (float) -- computational time per data point (normalized by batch_size)
            t_data (float) -- data loading time per data point (normalized by batch_size)
        """
        message = '(epoch: %d, iters: %d, comp-time: %.3f, data-time: %.3f) ' % (epoch, iters, t_comp, t_data)
        for k, v in losses.items():
            message += '%s: %.3f ' % (k, v)

        for k, v in metrics.items():
            message += '%s: %.3f ' % (k, v)

        print(message)  # print the message
        with open(self.log_name, "a") as log_file:
            log_file.write('%s\n' % message)  # save the message
